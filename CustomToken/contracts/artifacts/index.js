const customTokenContract = new web3.eth.Contract(contractABI, contractAddress);

async function getBalance(address) {
    try {
        const balance = await customTokenContract.methods.balanceOf(accountAddress).call();
        console.log(`Balance of ${accountAddress}:`, balance);
    } catch (error) {
        console.error('Error retrieving balance:', error.message || error);
    }
}

async function approveSpending(spender, tokens) {
    try {
        const transaction = await customTokenContract.methods.approve(spender, tokens).send({ from: accountAddress });
        console.log('Approval Transaction:', transaction);
    } catch (error) {
        console.error('Error during approval:', error.message || error);
    }
}

async function transferTokens(to, tokens) {
    try {
        const transaction = await customTokenContract.methods.transfer(to, tokens).send({ from: accountAddress });
        console.log('Transfer Transaction:', transaction);
    } catch (error) {
        console.error('Error during transfer:', error.message || error);
    }
}

async function transferTokensFrom(from, to, tokens) {
    try {
        const transaction = await customTokenContract.methods.transferFrom(from, to, tokens).send({ from: accountAddress });
        console.log('Transfer From Transaction:', transaction);
    } catch (error) {
        console.error('Error during transfer from:', error.message || error);
    }
}
